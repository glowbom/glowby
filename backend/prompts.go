// getImageSourceText picks "Glowby Images" or "Lorem Picsum"
func getImageSourceText(imageSource string) string {
	if isGlowbyImagesSource(imageSource) {
		return "Glowby Images"
	}
	return "Lorem Picsum"
}

func buildDetailedTaskDescription(template, imageSource, userPrompt string) string {
	// get the base "Lorem Picsum" or "Glowby Images"
	imageSourceText := getImageSourceText(imageSource)
	imageDirective := fmt.Sprintf("You should also show relevant real pictures from %s website to make the generation more beautiful.", imageSourceText)

	// if "Glowby Images", append the extra instructions
	if isGlowbyImagesSource(imageSource) {
		imageDirective = `For imagery, use ONLY glowbyimage:<prompt> placeholders.
Don't place glowbyimage: in <img src>; keep every image as src='about:blank' in HTML, then assign src in JavaScript.
For every image, declare a unique const variable with a descriptive prompt, for example:
const imgHero = 'glowbyimage:Snowy Norwegian fjord at sunrise';
const imgGallery01 = 'glowbyimage:Colorful fishing village in Lofoten';

Then assign each variable to a matching element id in JavaScript:
document.getElementById('imgHero').src = imgHero;
document.getElementById('imgGallery01').src = imgGallery01;

Glowby Image Contract (mandatory):
1. Every glowbyimage variable must be assigned to an existing <img id="..."> element.
2. Never output data: URIs, base64 blobs, blob: URLs, object URLs, or inline binary image payloads.
3. Never place executable code on the same line as a // comment.
4. Do not minify JavaScript or CSS; keep human-readable spacing and indentation.
5. Use a normal <script> block for assignments (no imports required).`
	}

	// If user provided a template, check if userPrompt contains HTML (translation mode)
	// If userPrompt contains HTML, it's a translation request - return the full userPrompt
	// Otherwise fallback to default instructions for draw-to-code
	if template != "" {
		// Check if this is a translation request (userPrompt contains HTML code)
		if strings.Contains(userPrompt, "HTML code to translate") || strings.Contains(userPrompt, "<!DOCTYPE") || strings.Contains(userPrompt, "<html") {
			// Translation mode: userPrompt already contains the full prompt with HTML
			return userPrompt
		}
		// Draw-to-code mode with template hint
		return template
	} else {
		return fmt.Sprintf(`Turn this into a single HTML file using Tailwind.
%s
Add working JavaScript for interactive features.
The user describes this image as: %s

Remember: Start your response with [PROJECT_NAME: Your Title] before any HTML code.`, imageDirective, userPrompt)
	}
}

// buildDrawToCodeSystemPrompt merges the drawToCodeSystemPrompt with placeholders
func buildDrawToCodeSystemPrompt(template, imageSource string) string {
	basePrompt := drawToCodeSystemPrompt
	imageSourceText := getImageSourceText(imageSource)

	glowbyImagesInstructions := ""
	if isGlowbyImagesSource(imageSource) {
		glowbyImagesInstructions = `Using Glowby Images means you must declare JS variables like const img1 = 'glowbyimage:ADD_PROMPT_HERE'. In HTML, set every <img src> to 'about:blank', then assign .src in JavaScript (for example: document.getElementById('img1').src = img1). Every declared glowbyimage variable must be assigned to a matching <img id="...">. Never output base64/data/blob/object URLs. Keep JS/CSS human-readable (not minified), and never put executable code on the same line as a // comment.`
	}

	basePrompt = strings.ReplaceAll(basePrompt, "{{IMAGE_SOURCE}}", imageSourceText)
	basePrompt = strings.ReplaceAll(basePrompt, "{{GLOWBY_IMAGES_INSTRUCTIONS}}", glowbyImagesInstructions)

	// If user provided a "template", in local.ts we pick systemPromptTranslation instead
	if template != "" {
		return systemPromptTranslation
	}
	return basePrompt
}

// getSystemPrompt decides if we use the "translation" or "drawToCode" prompt
func getSystemPrompt(template, imageSource string) string {
	// If there's a template, local.ts picks systemPromptTranslation
	// else we do the normal drawToCodeSystemPrompt
	return buildDrawToCodeSystemPrompt(template, imageSource)
}
